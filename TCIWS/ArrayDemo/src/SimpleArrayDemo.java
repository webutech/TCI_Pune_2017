import java.util.Date;



public class SimpleArrayDemo {

	public static void main(String[] args) {
		//Array Creation
		int [] arr;
		// Array Instantiation
		arr=new int[5];
		
		// Array Assignment
		
		arr[0]=1;
		arr[1]=1;		
		System.out.println(arr);		
		for (int i=0;i<arr.length;i++) {
			System.out.println(arr[i]);
			
		}

	}

}
